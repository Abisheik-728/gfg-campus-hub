const BLOG_STORAGE_KEY = "gfg_admin_blogs";

export function getAdminBlogs(): BlogPost[] {
  try {
    return JSON.parse(localStorage.getItem(BLOG_STORAGE_KEY) || "[]");
  } catch { return []; }
}

export function saveAdminBlogs(blogs: BlogPost[]): void {
  localStorage.setItem(BLOG_STORAGE_KEY, JSON.stringify(blogs));
}

export function getAllBlogs(): BlogPost[] {
  const adminBlogs = getAdminBlogs();
  return [...defaultBlogPosts, ...adminBlogs];
}

export function getPublishedBlogs(): BlogPost[] {
  return getAllBlogs().filter(b => b.status === "published");
}

